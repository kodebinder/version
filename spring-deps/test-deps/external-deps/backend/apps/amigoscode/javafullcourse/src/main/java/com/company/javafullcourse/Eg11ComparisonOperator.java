package com.company.javafullcourse;

public class Eg11ComparisonOperator {
    public static void main(String[] args) {
        int first = 18;
        int second = 20;
        boolean isFirstGreaterThanSecond = first >= second;
        boolean isFirstLesserThanSecond = first <= second;
        System.out.println(isFirstGreaterThanSecond);
        System.out.println(isFirstLesserThanSecond);
        System.out.println(first == second);
        System.out.println(first != second);
    }
}
