package com.company.eg003.basiccalculator;

public class BasicCalculator {
    public static void main(String[] args) {
        int n1 = 9;
        int n2 = 4;

        System.out.println(n1 + n2);
        System.out.println(n1 - n2);
        System.out.println(n1 * n2);
        System.out.println(n1 / n2);
        System.out.println(n1 % n2);
    }
}
